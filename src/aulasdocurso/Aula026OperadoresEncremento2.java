public class Aula026OperadoresEncremento2 {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		int i = 0;
		int y = 0;
		//incremento
		//pre-incremento
		System.out.println(++i);
		
		
		//pós-incremento
		System.out.println(y++);
		System.out.println("");
		
		
		
		//decremento
		int a = 0;
		int b = 0;
		
		System.out.println(--a);
		b--;
		System.out.println(a--);
		System.out.println("");
		
		
		int u = 0;
		
		u = u + 10;//forma tradicional de fazer
		u += 10;//operador de atribuição
		u++;//operador de encremento
		
		
		
		
	}

}
