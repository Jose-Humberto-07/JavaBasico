package aulasdocurso;

public class Aula019TiposPrimitivos {

	public static void main(String[] args) {
		//1 Byte = 8 bits (só lembrando) 
		
		//Tipos Primitivos no Java
		
		
		//por padrão ele sempre será false
		//oucupa 1 byte, ou 8 bits
		boolean b = true;
		
		//por padrão o char é igual '' (ou seja, vazio)
		//ocupa 2 bytes, 16 bits
		char c = 'a';
		char c2 = '1';
		
		
		//por padrão vem 0 a nossa variável
		//byte ocupa 1 byte de memória
		//em um byte de memória é permitido 255 números
		//-128 e +127
		byte Byte = 100;
		byte bb = 127;
		
		
		
		//por padrão vem 0 a nossa variável
		//-32768 até +32767
		short camisa = 2000;
		
		//por padrão vem 0 a nossa variável
		//-2,147,483,648 até +2,147,486,647
		int I = 1;
		
		//numeros muitos grandes, muito mesmo
		long l = 1;
		
		
		float f = 4.4f;
		
		double d = 2.2;
		
		
		
		
		
		
		
		
		
		
		
	}

}
